public class 출력문_1 {
    public static void main(String[] args){
    //메인 메서드 안에서 내용쓰기, 메인 메서드 안이 실행된다.
    //public static void main(String[] args)반드시 외우기
    //단축키는 main + 엔터


    // 소괄호안의 내용을 출력한 뒤 한 줄 개행 하는 입력어.
    // System.out.println , print();
    // 단축키는 sout + 엔터
    // 항상 뒤에 ; 넣기
    // Ln은 라인의 줄임말 한 줄 개행하겠다는 소리
      System.out.println("hello~");
      System.out.println("hello~");

      System.out.print("hello~");
      System.out.print("hello~");
    // 소괄호 안의 내용을 출력만 한다.

      System.out.println();
    //한 줄 개행만 한다.(소괄호 안에 내용을 넣지 않는다.)

      System.out.println(5);
    //숫자를 출력할때는 쌍따옴표 넣지 않는다.
    //문자를 출력하려면 쌍따옴표 넣어야한다.

      System.out.println("5");
    //쌍따옴표가 붙으면 문자로 인식, 붙지 않으면 연산으로 인식
    }
}
